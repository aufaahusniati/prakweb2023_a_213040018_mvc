MVC (Model-View-Controller)
- "Pola arsitektur pada perancangan perangkat lunak berorientasi objek"
- Memisahkan antaran tampilan, data, dan proses
- Model (Data) : Representasi pengetahuan, mengelola data, logika bisnis 
- Controller (Proses) : Perantara antara model dan view, menangani pemrosesan pada aplikasi, menangani aksi dari user
- View (Tampilan) : Output apa yang dilihat oleh user, representasi visual dari model, lapisan representasi

Kenapa MVC?
- Organisasi dan struktur kode
- Pemisahan logic dan tampilan
- Perawatan kode
- Implementasi konsep OOP yang sudah dipelajari
- Digunakan oleh banyak web application framework

Framework MVC : PHP (codelgniter, laravel, yii, dll)

